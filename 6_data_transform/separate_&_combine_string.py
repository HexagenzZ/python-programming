# join()
# method ini untuk mengganbungkan sesuatu yang terpisah
# Example :
print(" ".join(["Its", "Not", "Make", "Sense"]))
# desc : list di atas digabungkan menjadi satu kalimat (string) deang join
# dan dapat diperhatikan juga terdapat " " (double quote yang terdapat spasi di dalamnya)
# hal tersebut mrupakan delimiter yang digunakan untuk menambahkan suatu karakter yang ingin dimasukkan pada saat join

# kita juga bisa menambahkan delimiter lain
var = ["Saya", "akan", "lawan"]
print("YNTKTS".join(var))

