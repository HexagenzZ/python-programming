# transformasi yang dilakukan disini adalah mengubah menjadi kapital -> upper()
# atau mengubah nya menjadi huruf kecil -> lower()
# kedua method ini merupakan bawaan dari python
# kalau karakter yang dilakukan perubahan bukan huruf (angka, simbol dll) maka transformasi tidak akan dilakukan

# upper()
# Example :
kata = "blade is daebak"
kata = kata.upper()
print(kata)

# lower()
# Example :
kataka = "BLADE IS DAEBAK"
kataka = kataka.lower()
print(kataka)
